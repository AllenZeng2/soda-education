package com.soda.entity;

import java.util.Date;

public class Discuss {
	private Integer ds_id;
	private String ds_content;
	private Date ds_showtime;
	private Integer ds_newsId;
	private Integer ds_status;
	private String ds_other1;
	private String ds_other2;

	public Discuss() {
		super();
	}

	public Discuss(Integer ds_id, String ds_content, Date ds_showtime, Integer ds_newsId, Integer ds_status, String ds_other1, String ds_other2) {
		this.ds_id = ds_id;
		this.ds_content = ds_content;
		this.ds_showtime = ds_showtime;
		this.ds_newsId = ds_newsId;
		this.ds_status = ds_status;
		this.ds_other1 = ds_other1;
		this.ds_other2 = ds_other2;
	}

	public void setDs_id(Integer ds_id) {
		this.ds_id = ds_id;
	}

	public Integer getDs_id(){
		return ds_id;
	}


	public void setDs_content(String ds_content) {
		this.ds_content = ds_content;
	}

	public String getDs_content(){
		return ds_content;
	}


	public void setDs_showtime(Date ds_showtime) {
		this.ds_showtime = ds_showtime;
	}

	public Date getDs_showtime(){
		return ds_showtime;
	}


	public void setDs_newsId(Integer ds_newsId) {
		this.ds_newsId = ds_newsId;
	}

	public Integer getDs_newsId(){
		return ds_newsId;
	}


	public void setDs_status(Integer ds_status) {
		this.ds_status = ds_status;
	}

	public Integer getDs_status(){
		return ds_status;
	}


	public void setDs_other1(String ds_other1) {
		this.ds_other1 = ds_other1;
	}

	public String getDs_other1(){
		return ds_other1;
	}


	public void setDs_other2(String ds_other2) {
		this.ds_other2 = ds_other2;
	}

	public String getDs_other2(){
		return ds_other2;
	}

}

